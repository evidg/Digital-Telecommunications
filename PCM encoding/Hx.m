%Συνάρτηση που μας υπολογίζει την εντροπία H(x)
%Ο τύπος της είναι για κάθε ένα σύμβολο ο υπολογισμός του αθροίσματος 
% - p(i)*log2(p(i)).
function [H] = Hx(p)
H = 0; % αρχικοποιώ με μηδέν 
for i=1:length(p)
    if (p(i) ~= 0)          
        H = H - p(i)*log2(p(i)); % υπολογισμός εντροπίας 
    end
end
